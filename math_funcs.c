#include <stdio.h>
#include <math.h>

int main()
{
    double power = pow(2, 3);
    double square = sqrt(36);
    double ceil_value = ceil(36.7);
    double floor_value = floor(36.7);
    printf("%f \n", power);
    printf("%f \n", square);
    printf("%f \n", ceil_value);
    printf("%f \n", floor_value);
    return 0;
}